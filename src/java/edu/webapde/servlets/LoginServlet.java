    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int idnum = Integer.parseInt(request.getParameter("idnum"));
		String password = request.getParameter("password");
		
		//getting all users
		ArrayList<User> userList = new UserRetriever().getAllUsers();
		request.setAttribute("userList", userList);
		
		//comparing the idnum inputed to the idnumber of every user
		for(int i=0 ; i<userList.size() ; i++){
			if(userList.get(i).getIdnum()==idnum){
				
				HttpSession session = request.getSession();
				session.setAttribute("currentUser", userList.get(i).getFirstname());
				
				Cookie c = new Cookie("currentUser", userList.get(i).getFirstname());
				c.setMaxAge(60*60*24);
				response.addCookie(c);
				
				RequestDispatcher dispatcher = request.getRequestDispatcher("home.jsp");
				dispatcher.forward(request, response);
			}
			else{
				RequestDispatcher dispatcher = request.getRequestDispatcher("index.jsp");
			}
		}
    }

